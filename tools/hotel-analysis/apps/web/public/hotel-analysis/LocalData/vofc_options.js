// Helper function to get option by ID
window.getOptionById = function(ofcNumber) {
  return window.VOFC_OPTIONS.find(o => o.ofc_number === ofcNumber);
};

// Helper function to get options by category
window.getOptionsByCategory = function(category) {
  return window.VOFC_OPTIONS.filter(o => o.category === category);
};

// Helper function to get options by priority
window.getOptionsByPriority = function(priority) {
  return window.VOFC_OPTIONS.filter(o => o.priority === priority);
};

// Helper function to get options for specific vulnerability
window.getOptionsForVulnerability = function(vulnerabilityId) {
  return window.VOFC_OPTIONS.filter(o => o.vulnerability_id === vulnerabilityId);
};

